def can_jump(nums):
    """
    –ñ–∞–¥–Ω—ã–π –ø–æ–¥—Ö–æ–¥: –æ—Ç—Å–ª–µ–∂–∏–≤–∞–µ–º max_reach
    –í—Ä–µ–º—è: O(n), –ü–∞–º—è—Ç—å: O(1)
    """
    max_reach = 0

    for i in range(len(nums)):
        # –ï—Å–ª–∏ —Ç–µ–∫—É—â–∞—è –ø–æ–∑–∏—Ü–∏—è –Ω–µ–¥–æ—Å—Ç–∏–∂–∏–º–∞
        if i > max_reach:
            return False

        # –û–±–Ω–æ–≤–ª—è–µ–º –º–∞–∫—Å–∏–º–∞–ª—å–Ω—É—é –¥–æ—Å—Ç–∏–∂–∏–º—É—é –ø–æ–∑–∏—Ü–∏—é
        max_reach = max(max_reach, i + nums[i])

        # –†–∞–Ω–Ω–∏–π –≤—ã—Ö–æ–¥
        if max_reach >= len(nums) - 1:
            return True

    return True


