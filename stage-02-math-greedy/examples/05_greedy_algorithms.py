def greedy_coin_change(amount, coins):
    """
    –†–ê–ë–û–¢–ê–ï–¢ –¥–ª—è '–∫–∞–Ω–æ–Ω–∏—á–µ—Å–∫–∏—Ö' —Å–∏—Å—Ç–µ–º –º–æ–Ω–µ—Ç
    –ù–∞–ø—Ä–∏–º–µ—Ä: [1, 5, 10, 25]
    """
    coins.sort(reverse=True)  # –æ—Ç –±–æ–ª—å—à–µ–≥–æ –∫ –º–µ–Ω—å—à–µ–º—É

    count = 0
    result = []

    for coin in coins:
        num_coins = amount // coin
        if num_coins > 0:
            count += num_coins
            result.extend([coin] * num_coins)
            amount -= coin * num_coins

        if amount == 0:
            break

    return count, result


