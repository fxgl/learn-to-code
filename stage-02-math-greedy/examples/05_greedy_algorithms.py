def fractional_knapsack(items, capacity):
    """
    items = [(weight, value), ...]
    capacity = –≤–º–µ—Å—Ç–∏–º–æ—Å—Ç—å —Ä—é–∫–∑–∞–∫–∞
    –ú–û–ñ–ù–û –±—Ä–∞—Ç—å –¥—Ä–æ–±–Ω—ã–µ —á–∞—Å—Ç–∏ –ø—Ä–µ–¥–º–µ—Ç–æ–≤
    """
    # –°–æ—Ä—Ç–∏—Ä—É–µ–º –ø–æ value/weight (—É–±—ã–≤–∞–Ω–∏–µ)
    items.sort(key=lambda x: x[1] / x[0], reverse=True)

    total_value = 0.0
    details = []

    for weight, value in items:
        value_per_unit = value / weight

        if capacity >= weight:
            # –ë–µ—Ä—ë–º –≤–µ—Å—å –ø—Ä–µ–¥–º–µ—Ç
            total_value += value
            capacity -= weight
            details.append(f"–í–µ—Å—å –ø—Ä–µ–¥–º–µ—Ç ({weight}–∫–≥, {value}‚ÇΩ) ‚Üí +{value}‚ÇΩ")
        else:
            # –ë–µ—Ä—ë–º —á–∞—Å—Ç—å –ø—Ä–µ–¥–º–µ—Ç–∞
            fraction = capacity / weight
            added_value = value * fraction
            total_value += added_value
            details.append(f"–ß–∞—Å—Ç—å –ø—Ä–µ–¥–º–µ—Ç–∞ ({capacity:.1f}–∫–≥ –∏–∑ {weight}–∫–≥) ‚Üí +{added_value:.1f}‚ÇΩ")
            break

    return total_value, details


