def can_complete_circuit(gas, cost):
    """
    gas[i] - –±–µ–Ω–∑–∏–Ω –Ω–∞ i-–π –∑–∞–ø—Ä–∞–≤–∫–µ
    cost[i] - —Å—Ç–æ–∏–º–æ—Å—Ç—å –¥–æ —Å–ª–µ–¥—É—é—â–µ–π
    –í–µ—Ä–Ω—É—Ç—å –∏–Ω–¥–µ–∫—Å —Å—Ç–∞—Ä—Ç–æ–≤–æ–π –∑–∞–ø—Ä–∞–≤–∫–∏ –∏–ª–∏ -1
    """
    total_gas = 0
    total_cost = 0
    tank = 0
    start = 0

    for i in range(len(gas)):
        total_gas += gas[i]
        total_cost += cost[i]
        tank += gas[i] - cost[i]

        # –ï—Å–ª–∏ –±–∞–∫ –æ–ø—É—Å—Ç–µ–ª
        if tank < 0:
            # –ù–∞—á–∏–Ω–∞–µ–º —Å–æ —Å–ª–µ–¥—É—é—â–µ–π —Å—Ç–∞–Ω—Ü–∏–∏
            start = i + 1
            tank = 0

    # –†–µ—à–µ–Ω–∏–µ —Å—É—â–µ—Å—Ç–≤—É–µ—Ç, –µ—Å–ª–∏ total_gas >= total_cost
    return start if total_gas >= total_cost else -1


